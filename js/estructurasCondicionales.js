let numero = parseInt(prompt('Ingrese un numero'));
//if(condicion logica)
if (numero > 10) {
    document.write('El numero ingresado es mayor a 10 - numero : ' + numero);
} else if (numero == 10) {
    document.write('El numero ingresado es igual a 10');
} else {
    document.write('El numero ingresado es menor - numero : ' + numero);
}
